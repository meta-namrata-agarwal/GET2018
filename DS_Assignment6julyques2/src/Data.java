/**
 * Data class to use as list for string and count of unique charater
 * 
 * @author User20
 *
 */
public class Data {
    String string;
    int count;

    public Data(String string, int count) {
        this.string = string;
        this.count = count;
    }

    public String getString() {
        return string;
    }

    public int getcount() {
        return count;
    }
}
